    private void btnAdelanteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdelanteActionPerformed

        // Capturo el textFile lo convierto a int lo incremento y lo vuelvo a convertir en String para poder mostarlo
        String indice = txtIndice.getText();
        int ind = Integer.parseInt(indice);
       
        if (ind < 9) {
        ind++;
        indice = String.valueOf(ind);
        txtIndice.setText(indice);
        
        txtDni.setText(dni[ind]);
        txtNombre.setText(nombre[ind]);
        textApellido.setText(apellido[ind]);
        txtDireccion.setText(direccion[ind]);
        txtTelefono.setText(telefono[ind]);
        txtNacimiento.setText(fechaNac[ind]);
        }
    }//GEN-LAST:event_btnAdelanteActionPerformed

    private void btnAtrasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtrasActionPerformed
        
// Capturo el textFile lo convierto a int lo decremento y lo vuelvo a convertir en String para poder mostarlo
    
        String indice = txtIndice.getText();
        int ind = Integer.parseInt(indice);
       
        if (ind > 0) {
        ind--;
        indice = String.valueOf(ind);
        txtIndice.setText(indice);
        
        txtDni.setText(dni[ind]);
        txtNombre.setText(nombre[ind]);
        textApellido.setText(apellido[ind]);
        txtDireccion.setText(direccion[ind]);
        txtTelefono.setText(telefono[ind]);
        txtNacimiento.setText(fechaNac[ind]);
        }
    }//GEN-LAST:event_btnAtrasActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        
        String indice = txtIndice.getText();
        int ind = Integer.parseInt(indice);
        
        dni[ind] = txtDni.getText();
        nombre[ind] = txtNombre.getText();
        apellido[ind] = textApellido.getText();
        direccion[ind] = txtDireccion.getText();
        telefono[ind] = txtTelefono.getText();
        fechaNac[ind] = txtNacimiento.getText();
        
    }//GEN-LAST:event_btnGuardarActionPerformed


